@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly lang = signal<LangCode>('lv');

  constructor() {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'en' || saved === 'lv') this.lang.set(saved);
  }

  setFromInterfaceLanguage(language: Language | undefined | null) {
    this.setLanguage(language === Language.ENGLISH ? 'en' : 'lv');
  }

  setLanguage(lang: LangCode) {
    this.lang.set(lang);
    localStorage.setItem(STORAGE_KEY, lang);
  }

  t(key: string): string {
    if (this.lang() === 'en') return translations.en[key] ?? key;
    return translations.lv[key] ?? key;
  }
}

